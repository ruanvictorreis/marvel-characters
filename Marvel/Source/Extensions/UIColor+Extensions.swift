    }
    
    public class var darkGreen: UIColor {
        return UIColor(red: 0.16, green: 0.26, blue: 0.17, alpha: 1)
    }
    
    public class var lightGreen: UIColor {
        return UIColor(red: 0.39, green: 0.8, blue: 0.51, alpha: 1)
    }
    
    public class var darkYellow: UIColor {
        return UIColor(red: 0.25, green: 0.24, blue: 0.09, alpha: 1)
    }
    
    public class var lightYellow: UIColor {
        return UIColor(red: 0.83, green: 0.83, blue: 0.32, alpha: 1)
    }
    
    public class var darkRed: UIColor {
        return UIColor(red: 0.31, green: 0.10, blue: 0.20, alpha: 1)
    }
    
    public class var lightRed: UIColor {
        return UIColor(red: 0.79, green: 0.22, blue: 0.38, alpha: 1)
    }
